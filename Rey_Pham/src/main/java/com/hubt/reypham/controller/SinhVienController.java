    public void loadTable()throws SQLException{
        model =(DefaultTableModel) tbl.getModel();
        model.setRowCount(0);
        for (SinhVien x : dao.findAll()) {
            model.addRow(new Object[]{
            x.getMaSv(),x.getHoTen(),x.getMaLop(),x.getGioiTinh()==0 ?"Nam":"nữ",x.getNgaySinh(),x.getDiaChi()});
        }
    }
    public void loadCombobox() throws SQLException{
        for (DmLop x : dmLopDAO.findAll()) {
            cbb_maLop.addItem(x.getMaLop());
        }
    }

