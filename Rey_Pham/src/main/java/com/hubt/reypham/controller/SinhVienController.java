    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       SinhVien sv;
        sv = new SinhVien(
                txt_maSv.getText(),
                txt_hoTen.getText(),
                cbb_maLop.getSelectedItem()+"",
                rdo_nam.isSelected()?0:1,
                Date.valueOf(txt_ngaySinh.getText()),
                txt_diaChi.getText());
        try {
            if(dao.create(sv) >0){
                JOptionPane.showMessageDialog(this, "thêm thành công");
                loadTable();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            SinhVien sv = dao.findByMa(txt_maSv.getText());
            sv.setGioiTinh(rdo_nam.isSelected()?0:1);
            sv.setDiaChi(txt_diaChi.getText());
            sv.setNgaySinh(Date.valueOf(txt_ngaySinh.getText()));
            if(dao.updateByMa(txt_maSv.getText(), sv)>0){
                 JOptionPane.showMessageDialog(this, "sửa thành công");
                loadTable();
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            if(dao.deleteByMa(txt_maSv.getText())>0){
                 JOptionPane.showMessageDialog(this, "Xóa thành công");
                loadTable();
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
     int index = tbl.getSelectedRow();
     txt_maSv.setText(tbl.getValueAt(index, 0).toString());
     txt_hoTen.setText(tbl.getValueAt(index, 1).toString());
     cbb_maLop.setSelectedItem(tbl.getValueAt(index, 2));
     txt_ngaySinh.setText(tbl.getValueAt(index, 4).toString());
     txt_diaChi.setText(tbl.getValueAt(index, 5).toString());
    }//GEN-LAST:event_tblMouseClicked

