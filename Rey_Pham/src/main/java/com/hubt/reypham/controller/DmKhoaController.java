    public void loadTable()throws SQLException{
        model = (DefaultTableModel) tbl.getModel();
        model.setRowCount(0);
        for (DmKhoa x :dao.findAll() ) {
            model.addRow(new Object[]{
            x.getMaKhoa(),x.getTenKhoa()});
        }
    }

