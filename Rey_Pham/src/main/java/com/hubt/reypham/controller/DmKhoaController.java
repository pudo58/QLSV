    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
      int index = tbl.getSelectedRow();
      txt_maKhoa.setText(tbl.getValueAt(index, 0).toString());
      txt_tenKhoa.setText(tbl.getValueAt(index, 1).toString());
      
    }//GEN-LAST:event_tblMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     DmKhoa khoa = new DmKhoa(txt_maKhoa.getText(), txt_tenKhoa.getText());
        try {
            if(dao.create(khoa) > 0){
                JOptionPane.showMessageDialog(this, "thêm thành công");
                loadTable();
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, "thêm thất bại" + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            if(dao.updateByMa(txt_maKhoa.getText(), txt_tenKhoa.getText())>0){
                JOptionPane.showMessageDialog(this, "cập nhật thành công");
                loadTable();
            }
                
        } catch (SQLException e) {
             JOptionPane.showMessageDialog(this, "cập nhật thất bại" + e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            if(dao.deleteByMa(txt_maKhoa.getText())>0){
                 JOptionPane.showMessageDialog(this, "Xóa thành công" );
                 loadTable();
            }
        } catch (HeadlessException | SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

