    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        int index = tbl.getSelectedRow();
        txt_maHocPhan.setText(tbl.getValueAt(index, 0).toString());
        txt_tenHocPhan.setText(tbl.getValueAt(index, 1).toString());
        txt_soDvhp.setText(tbl.getValueAt(index, 2).toString());
        cbb_maNganh.setSelectedItem(tbl.getValueAt(index, 3));
        cbb_hocKy.setSelectedItem(tbl.getValueAt(index, 4));
    }//GEN-LAST:event_tblMouseClicked

