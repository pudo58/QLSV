    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String mahp= txt_maHocPhan.getText();
    String tenhp = txt_tenHocPhan.getText();
    String soDvhp = txt_soDvhp.getText();
    String maNganh = (String) cbb_maNganh.getSelectedItem();
    String hocKy= (String) cbb_hocKy.getSelectedItem();
    DmHocPhan hocPhan = new DmHocPhan(mahp, tenhp, soDvhp, maNganh, hocKy);
        try {
            if(dao.create(hocPhan) > 0){
                JOptionPane.showMessageDialog(this, "thêm thành công");
                loadtable();
            }   
        } catch (SQLException ex) {
        }
    
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            DmHocPhan dhp = dao.findByma(txt_maHocPhan.getText());
            dhp.setHocKy(cbb_hocKy.getSelectedItem().toString());
            dhp.setSoDvhp(txt_soDvhp.getText());
            dhp.setTenHp(txt_tenHocPhan.getText());
            if(dao.updateByMa(txt_maHocPhan.getText(), dhp) >0){
                JOptionPane.showMessageDialog(this, "Cập nhật thành công");
                loadtable();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "cập nhật thất bại" + e.getMessage());
        }
      
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            if(dao.deleteByMa(txt_maHocPhan.getText())>0){
                JOptionPane.showMessageDialog(this, "Xóa thành công");
                loadtable();
            }
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, "Xóa thất bại");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

